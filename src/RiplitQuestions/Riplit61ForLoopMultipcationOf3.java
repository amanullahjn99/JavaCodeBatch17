package RiplitQuestions;

public class Riplit61ForLoopMultipcationOf3 {
    public static void main(String[] args) {
        int multiplication = 3;

        for (int i = 1; i <= 10; i++) {
            int result = multiplication * i;
            System.out.println(multiplication + "*" + i + "=" + result);
        }
    }
}
