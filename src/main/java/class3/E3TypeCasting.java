package class3;

public class E3TypeCasting {
    public static void main(String[] args) {
        //byte->short->int->long->float->double

        int number=126;
        byte number2;
        number2=(byte)number;
        System.out.println(number2);

        short box1=2000;
        int box2=box1;
        System.out.println(box2);

    }
}
