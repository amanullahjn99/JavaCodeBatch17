public class E5RelationalOperatores {
    public static void main(String[] args) {
        System.out.println(10>15);  // is 10 greater than 15=false
        System.out.println(10<15);  // is 10 smaller than 15=ture
        System.out.println(10==15);  // is 10 equal to 15= false
        System.out.println(10!=15);  // is 10 not to 15= true
        System.out.println(10>=15);  // is 10 greater than or equal to 15= false
        System.out.println(10<=15);  // is 10 smaller than or equal to 15= ture

    }
}
