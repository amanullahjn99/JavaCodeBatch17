package class7;

public class T8ArraysAddingTheValueOfInt {
    /* just to print the even number from below arrays (20, 48, 60)

     */
    public static void main(String[] args) {

        int[] numbers = {20, 10, 50, 30, 40};   //150
        int sum=0;
        for (int i = 0; i < numbers.length; i++) {
            sum=sum+numbers[i]; // pic the previous value of sum and add it to current value of arrays

        }
        System.out.println(sum);







    }

}


