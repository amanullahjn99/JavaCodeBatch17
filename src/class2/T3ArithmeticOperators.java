package class2;

public class T3ArithmeticOperators {
    public static void main(String[] args) {
        double num1=10.5;
        double num2=3.5;
        double addResults=num1+num2;
        double subResults=num1-num2;
        double mulResults=num1*num2;
        double divResults=num1/num2;

        System.out.println("The Addition of two number is "+num1+" and "+num2+ " is equal to "+addResults);
        System.out.println("The Subtraction of two number is "+num1+" and "+num2+ " is equal to "+subResults);
        System.out.println("The Multiplication of two number is "+num1+" and "+num2+ " is equal to "+mulResults);
        System.out.println("The Division of two number is "+num1+" and "+num2+ " is equal to "+divResults);


    }
}
